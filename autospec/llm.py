class BedrockClient:
    """Stub for the AWS Bedrock-backed LLM client.

    The real implementation would call a Bedrock model (e.g. Anthropic Claude).
    This stub raises on use so the factory falls back to the deterministic
    client whenever Bedrock is not actually wired up.
    """

    def __init__(self, region: Optional[str] = None, model_id: Optional[str] = None):
        self.region = region
        self.model_id = model_id

    def complete(self, system: str, prompt: str, schema: Optional[dict] = None) -> str:
        """Raise to signal Bedrock is unavailable in this environment."""
        raise RuntimeError("BedrockClient is a stub; no Bedrock backend configured")


class DeterministicFallbackClient:
    """Returns curated, schema-valid responses for the tip-calculator demo.

    The ``schema`` argument carries a ``kind`` key telling the client which
    artifact to synthesize: "spec", "code", or "tests".
    """

    def complete(self, system: str, prompt: str, schema: Optional[dict] = None) -> str:
        """Return a JSON string for the requested artifact kind.

        Args:
            system: The agent system prompt (ignored by the fallback).
            prompt: The agent prompt (ignored by the fallback).
            schema: A dict with a ``kind`` of "spec", "code", or "tests".

        Returns:
            A JSON-encoded string the calling agent can parse into a model.
        """
        kind = (schema or {}).get("kind")
        if kind == "spec":
            return json.dumps(TIP_CALCULATOR_SPEC)
        if kind == "code":
            return json.dumps(
                {
                    "module_name": TIP_CALCULATOR_MODULE_NAME,
                    "source": TIP_CALCULATOR_SOURCE,
                }
            )
        if kind == "tests":
            criterion_ids = [
                c["id"]
                for req in TIP_CALCULATOR_SPEC["requirements"]
                for c in req["criteria"]
            ]
            return json.dumps(
                {"source": TIP_CALCULATOR_TESTS, "criterion_ids": criterion_ids}
            )
        raise ValueError("DeterministicFallbackClient: unknown request kind {0!r}".format(kind))


def make_client(prefer_bedrock: Optional[bool] = None) -> "object":
    """Select an LLM client, falling back silently to the deterministic one.

    Bedrock is attempted only when explicitly enabled via the
    ``AUTOSPEC_USE_BEDROCK`` environment variable (or ``prefer_bedrock=True``)
    and credentials/region are present. Any failure constructing or probing the
    Bedrock client results in a silent fallback so the demo always runs.

    Args:
        prefer_bedrock: Override for the env-var-based selection.

    Returns:
        An object implementing the ``complete`` method.
    """
    use_bedrock = prefer_bedrock
    if use_bedrock is None:
        use_bedrock = os.environ.get("AUTOSPEC_USE_BEDROCK", "").lower() in (
            "1",
            "true",
            "yes",
        )
    if use_bedrock:
        try:
            region = os.environ.get("AWS_REGION") or os.environ.get("AWS_DEFAULT_REGION")
            client = BedrockClient(region=region)
            # Probe with a trivial call; the stub raises, triggering fallback.
            client.complete("probe", "probe", {"kind": "probe"})
            return client
        except Exception:  # noqa: BLE001 - any Bedrock problem -> fallback
            pass
    return DeterministicFallbackClient()
