"""Deterministic artifact templates for the tip-calculator demo.

The Spec/Build/Test agents are LLM-backed in production, but for a rock-solid
offline demo these curated, schema-valid artifacts guarantee the pipeline always
runs end to end. The first build attempt deliberately omits input validation so
the self-correcting Review -> Build loop has something real to fix on stage.
"""

from __future__ import annotations

from .models import AcceptanceCriterion, Requirement, SpecDocument


def tip_calculator_spec() -> SpecDocument:
    """Return the structured spec the Spec Agent derives from the demo brief."""
    requirements = (
        Requirement(
            number=1,
            title="Compute tip, total, and per-person amounts",
            criteria=(
                AcceptanceCriterion(
                    id="1",
                    given="a bill total, a tip percentage, and a number of people",
                    when="the calculator runs",
                    then="it returns tip, total-with-tip, and per-person, each rounded to 2 decimals",
                ),
            ),
        ),
        Requirement(
            number=2,
            title="Zero-tip handling",
            criteria=(
                AcceptanceCriterion(
                    id="2",
                    given="a tip percentage of 0",
                    when="the calculator runs",
                    then="the tip is 0.00 and the total equals the bill total",
                ),
            ),
        ),
        Requirement(
            number=3,
            title="Even per-person split",
            criteria=(
                AcceptanceCriterion(
                    id="3",
                    given="a total-with-tip and a number of people",
                    when="the calculator runs",
                    then="the per-person amount is the total divided evenly by the people count",
                ),
            ),
        ),
        Requirement(
            number=4,
            title="Reject invalid party size",
            criteria=(
                AcceptanceCriterion(
                    id="4",
                    given="a number of people less than 1 or not an integer",
                    when="the calculator runs",
                    then="it rejects the input and raises a ValueError",
                ),
            ),
        ),
        Requirement(
            number=5,
            title="Reject negative money inputs",
            criteria=(
                AcceptanceCriterion(
                    id="5",
                    given="a negative bill total or a negative tip percentage",
                    when="the calculator runs",
                    then="it rejects the input and raises a ValueError",
                ),
            ),
        ),
    )
    edge_cases = (
        "Tip percentage of exactly 0 (no tip).",
        "Number of people equal to 0 or negative (invalid).",
        "Non-integer number of people (invalid).",
        "Negative bill total or tip percentage (invalid).",
        "Rounding boundaries such as 0.005 (round-half-up).",
    )
    return SpecDocument(requirements=requirements, edge_cases=edge_cases)


