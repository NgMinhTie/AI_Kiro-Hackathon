FIXED_SOURCE = '''\
"""Tip calculator (generated by AutoSpec Build Agent)."""

from decimal import Decimal, ROUND_HALF_UP


def _round2(value):
    """Round a numeric value to 2 decimal places using round-half-up."""
    return float(Decimal(str(value)).quantize(Decimal("0.01"), rounding=ROUND_HALF_UP))


def calculate_tip(bill_total, tip_percentage, num_people):
    """Return tip, total-with-tip, and per-person share for a bill.

    Args:
        bill_total: Pre-tip bill amount (must be >= 0).
        tip_percentage: Tip percentage from 0 to 100 (must be >= 0).
        num_people: Number of people splitting the bill (integer >= 1).
    Returns:
        A dict with keys 'tip', 'total', and 'per_person', each rounded to 2 decimals.
    """
    if not isinstance(num_people, int) or num_people < 1:
        raise ValueError("num_people must be an integer >= 1")
    if bill_total < 0 or tip_percentage < 0:
        raise ValueError("bill_total and tip_percentage must be >= 0")
    tip = _round2(bill_total * tip_percentage / 100)
    total = _round2(bill_total + tip)
    per_person = _round2(total / num_people)
    return {"tip": tip, "total": total, "per_person": per_person}
'''


